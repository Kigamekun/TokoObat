// Mock cart functionality
export const mockCart = {
  items: [],
  total: 0,
  
  addItem(medicine, quantity = 1) {
    const existingItem = this.items.find(item => item.id === medicine.id);
    if (existingItem) {
      existingItem.quantity += quantity;
    } else {
      this.items.push({ ...medicine, quantity });
    }
    this.updateTotal();
    this.saveToLocalStorage();
  },
  
  removeItem(medicineId) {
    this.items = this.items.filter(item => item.id !== medicineId);
    this.updateTotal();
    this.saveToLocalStorage();
  },
  
  updateQuantity(medicineId, quantity) {
    const item = this.items.find(item => item.id === medicineId);
    if (item) {
      item.quantity = Math.max(0, quantity);
      if (item.quantity === 0) {
        this.removeItem(medicineId);
      }
    }
    this.updateTotal();
    this.saveToLocalStorage();
  },
  
  updateTotal() {
    this.total = this.items.reduce((sum, item) => sum + (item.price * item.quantity), 0);
  },
  
  clear() {
    this.items = [];
    this.total = 0;
    this.saveToLocalStorage();
  },
  
  saveToLocalStorage() {
    localStorage.setItem('pharmacyCart', JSON.stringify({
      items: this.items,
      total: this.total
    }));
  },
  
  loadFromLocalStorage() {
    const saved = localStorage.getItem('pharmacyCart');
    if (saved) {
      const data = JSON.parse(saved);
      this.items = data.items || [];
      this.total = data.total || 0;
    }
  }
};

// Initialize cart from localStorage
mockCart.loadFromLocalStorage();